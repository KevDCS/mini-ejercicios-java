public class areaRectangulo {
  public static void main(String[] args) {
  // Calcular el área de un rectángulo usando variables para los lados
  double ladoA = 10.50;
  double ladoB = 11.35;
  double areaCuadrado = ladoA * ladoB;

  // Se imprimen los valores de ambos lados, y se muestra el área total del rectángulo
  System.out.println("Un lado del cuadrado mide: " + ladoA + "\nEl otro lado mide: " + ladoB + "\nEl área del rectángulo es: " + areaCuadrado);
  }
}
